package exercise;

// BEGIN
public class Point {
    private int x ;
    private int y;

    // Конструктор
    public Point(Integer x, Integer y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
// END
